package assingment1;

public class Assignment1 {

	public static void main(String[] args) {
		int x = 0;
		int y = 0;
		for (System.out.println("even numbers"); x <= 100; x++) {
			if (x % 2 == 0) {
				System.out.println(x);
			}
		}
		for (System.out.println("odd numbers"); y <= 100; y++) {
			if (y % 2 != 0) {
				System.out.println(y);
			}
		}
	}
}
