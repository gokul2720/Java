package com.gp.day3;

public class NarAndWide {

	public static void main(String[] args) {
		int x= 10;
		long y= x;
		System.out.println(y);
		
		long a= 100;
		int b= (int)a;
		System.out.println(b);
		
		int z= 'g';
		char f= 67;
		System.out.println(z);
		System.out.println(f);
		
		byte g= 127;
		byte h= 127;
		byte out= (byte) ((byte)g+h);
		System.out.println(out);
		

	}

}
